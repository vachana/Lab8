
/**
 * Class to represent a Point in a plane
 */
public class Point2D {
  private double x, y;

  public Point2D(double x, double y) {
    this.x = x;
    this.y = y;
  }

  /**
   * Get the value of x
   */
  public double getX() {
    return x;
  }

  /**
   * Get the value of y
   */
  public double getY() {
    return y;
  }
}